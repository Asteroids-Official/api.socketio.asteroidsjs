/**
 * Interface that represents the spaceship data.
 */
export interface ISpaceship {
  /**
   * Property that defines the spaceship color.
   *
   * @example
   * "#888888"
   */
  color: string

  /**
   * Property that defines the spaceship color name.
   *
   * @example
   * "grey"
   */
  colorName: string
}
